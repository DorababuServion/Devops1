public class LargerNumber {

	
	public static void main(String[] args) {
		
		int a =50;
		int b =80;
		int c =90;
		
		if (a>b && a>c) {
			System.out.println("a is largest number");
		}else if (b>c && b>a) {
			System.out.println("b is largest number");
		}else {
			System.out.println("c is largest number");
		}
		
		
		
	}
}
